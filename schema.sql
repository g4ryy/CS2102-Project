CREATE TABLE Employees(
	INTEGER eid PRIMARY KEY
	INTEGER did
	DATE resignedDate
	TEXT ename
	TEXT email UNIQUE
	INTEGER contact1
	INTEGER contact2
	INTEGER contact3
	FOREIGN KEY (did) REFERENCES Departments(did) ON DELETE NO ACTION;
);

CREATE TABLE Junior(
	INTEGER eid PRIMARY KEY
	FOREIGN KEY (eid) REFERENCES Employees(eid) ON DELETE CASCADE;

);

CREATE TABLE Booker(
	INTEGER eid PRIMARY KEY
	FOREIGN KEY (eid) REFERENCES Employees(eid) ON DELETE CASCADE
);

CREATE TABLE Senior(
	INTEGER eid PRIMARY KEY
	FOREIGN KEY (eid) REFERENCES Booker(eid) ON DELETE CASCADE
);

CREATE TABLE Manager(
	INTEGER eid PRIMARY KEY
	FOREIGN KEY (eid) REFERENCES Booker(eid) ON DELETE CASCADE
);